month = input('Type the number of the month (1 to 12): ');

switch(month)
    case 1
        fprintf('January\n')
    case 2
        fprintf('February\n')
    case 3
        fprintf('March\n')
    case 4
        fprintf('April\n')
    case 5
        fprintf('May\n')
    case 6
        fprintf('June\n')
    case 7
        fprintf('July\n')
    case 8
        fprintf('August\n')
    case 9
        fprintf('September\n')
    case 10
        fprintf('October\n')
    case 11
        fprintf('November\n')
    case 12
        fprintf('December\n')
    otherwise
        fprintf('Invalid month\n')
end
